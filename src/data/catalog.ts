export function getFlaconImage(category: PerfumeCategory, gender: Gender): string {
  if (category === "luxury") return "/images/catalog/flacon-luxury-50ml.png";
  if (gender === "damen") return "/images/catalog/flacon-light-70ml.png";
  if (gender === "herren") return "/images/catalog/flacon-dark-70ml.png";
  return "/images/catalog/flacon-light-30ml.png";
}

export function getFragranceGradient(notes: string): string {
  const lower = notes.toLowerCase();
  if (lower.includes("orientalisch") || lower.includes("würzig"))
    return "from-amber-50 to-orange-50";
  if (lower.includes("frisch") || lower.includes("zitrisch") || lower.includes("aquatisch"))
    return "from-cyan-50 to-sky-50";
  if (lower.includes("blumig") || lower.includes("pudrig"))
    return "from-pink-50 to-rose-50";
  if (lower.includes("holzig") || lower.includes("ledrig") || lower.includes("erdig"))
    return "from-stone-100 to-amber-50";
  if (lower.includes("süss") || lower.includes("gourmand"))
    return "from-orange-50 to-yellow-50";
  if (lower.includes("fruchtig"))
    return "from-red-50 to-pink-50";
  return "from-neutral-50 to-stone-50";
}

